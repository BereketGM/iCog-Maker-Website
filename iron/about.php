                    <h2>The Robosapien Overview</h2>
                    <img style="text-align: justify" class="image_wrapper image_fl" src="images/robosapianx.jpg" alt="image" />
                    <p><em>RoboSapien is a toy-like biomorphic robot designed by Mark Tilden and produced by WowWee toys. The RoboSapien is preprogrammed with moves, and also can be controlled by an infrared remote control included with the toy, or by either a personal computer equipped with an infrared transmitter or an infrared transmitter-equipped PDA.</br>
                        <h4>Overview</h4>
The toy is capable of a walking motion without recourse to wheels within its feet. It is also able to grasp objects with either of its hands, and is also able to throw grasped objects with mild force. It has a small loudspeaker unit, which can broadcast several different vocalizations.

Released in 2004, the product was very successful over the Christmas season, with over 1.5 million units sold between April and December 2004.[1]


The toy's remote control unit has a total of 21 different buttons. With the help of two shift buttons, a total of 67 different robot-executable commands are accessible.
<h4>User modifications</h4>
Mark Tilden designed the RoboSapien to be easily modified or hacked. The electronics inside the toy are easily accessed and clearly labeled. A growing community of hackers have devoted themselves to adding new functionality to the robot. Some hacks have added a live video camera, others an LED belt that displays text, headlights, a coil gun, speech synthesis, a "flamethrower" (a small torch) and more. A modified RoboSapien is also used in the 2009 revival of The Electric Company.
<h4>Other uses</h4>
At the German Open 2005 tournament two teams of three RoboSapiens each played the first Soccer match of humanoid robots worldwide. University of Osnabr√ºck played against a team from Albert Ludwig's University of Freiburg. Replacing the head by a PDA allowed the robot to perceive its environment with a camera, a control program could then react to this via the PDA's infrared sender.
<h4>Variants</h4>
In January 2007, two new Robosapien variants were introduced at the Toy Fair in London, styled after Spider-Man and Homer Simpson, respectively. The Spidersapien features Spider-Man styled armor, and an array of Spider-Man sound effects. It was launched together with Spider-Man 3. Homersapien was launched to coincide with the release of The Simpsons Movie. He is similarly adapted with sound bites and a sculpted Homer head, as well as a unique Simpsons accessory and packaging. Also Robosapien comes in chrome red, blue, silver, gold, green, and also clear, other than the original white.

Many other Animals in the Robo series were created. These included "Robo-Pet" (A dog like creature) and "Robo-Raptor" (A Dinosaur like robot). In early 2013, WowWee announced the "Robosapien X", a Robosapien that can be controlled via any iOS device.</br>
